function checkMeaningRadical(kanji) {
    let foundRadical = false;

    for (const [radical, data] of Object.entries(meaningRadicalDatabase)) {
        if (data.kanjiList.includes(kanji)) {
            updateMeaningRadicalBox(data.radical, radical); // Use the correct parameters
            foundRadical = true;
            break; // Break once the radical is found
        }
    }

    if (!foundRadical) {
        // Special check for multiple entries with same character
        if (kanji === '月' || kanji === '阝') {
            for (const [radical, data] of Object.entries(meaningRadicalDatabase)) {
                if (data.radical === kanji) {
                    updateMeaningRadicalBox(radical, data.radical); // Swap parameters
                    foundRadical = true;
                    break;
                }
            }
        }
    }

    if (!foundRadical) {
        updateMeaningRadicalBox("", ""); // Clear the box if not found
    }
}


